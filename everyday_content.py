def _pick_tags(theme, rng):
    """テーマ固有タグを主にしつつ、汎用タグを1〜2個だけ混ぜて毎回少し変える。"""
    tags = list(theme["tags"])
    rng.shuffle(tags)
    tags = tags[: rng.randint(3, len(tags))]
    extra = rng.sample(GENERIC_TAGS, rng.randint(1, 2))
    for e in extra:
        if e not in tags:
            tags.append(e)
    return tags


def build_article(seed=None):
    """日常記事を1本組み立てて返す。

    Returns: dict(title, category, tags, body_html)
      body_html は画像・末尾リンクを含まない本文パラグラフのみ（<p>...</p> の連なり）。
    """
    rng = random.Random(seed)
    theme = rng.choice(THEMES)

    title = rng.choice(theme["titles"])
    intro = rng.choice(theme["intros"])
    body = rng.choice(theme["bodies"])
    close = rng.choice(theme["closes"])

    paragraphs = [intro, body, close]
    # たまに本題をもう1段落増やして、記事の長さにも揺らぎを持たせる
    if rng.random() < 0.4:
        extra_body = rng.choice([b for b in theme["bodies"] if b != body] or [body])
        paragraphs.insert(2, extra_body)

    body_html = "".join(f"<p>{html.escape(p)}</p>" for p in paragraphs)

    return {
        "title": title,
        "category": theme["category"],
        "tags": _pick_tags(theme, rng),
        "body_html": body_html,
    }


